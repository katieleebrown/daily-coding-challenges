function greet(language) {
	switch (language) {
      case 'english':
        return 'Welcome'
        break
      case 'czech':
        return 'Vitejte'
        break
      case 'dutch':
        return 'Welkom'
        break
      case 'danish':
        return 'Velkomst'
        break
      case 'estonian':
        return 'Tere tulemast'
        break
      case 'finnish':
        return 'Tervetuloa'
        break;
      case 'flemish':
        return 'Welgekomen'
        break
      case 'french':
        return 'Bienvenue'
        break
      case 'german':
        return 'Willkommen'
        break
      case 'irish':
        return 'Failte'
        break
      case 'italian':
        return 'Benvenuto'
        break
      case 'latvian':
        return 'Gaidits'
        break
      case 'lithuanian':
        return 'Laukiamas'
        break
      case 'polish':
        return 'Witamy'
        break
      case 'spanish':
        return 'Bienvenido'
        break
      case 'swedish':
        return 'Valkommen'
        break
      case 'welsh':
        return 'Croeso'
        break   
      default:
        return 'Welcome'
        break
  }
}